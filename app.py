    st.error("–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–µ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã (–¥–∏–∞–º–µ—Ç—Ä—ã/–ø–ª–æ—â–∞–¥–∏/—Ç–æ–ª—â–∏–Ω—ã/—Ç–µ–ø–ª–æ–ø—Ä–æ–≤–æ–¥–Ω–æ—Å—Ç–∏).")
    st.stop()

