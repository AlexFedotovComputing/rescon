dT_sys = Tw_K - Tair_K

D_i = D_i_mm * 1e-3
S = S_mm2 * 1e-6
t_r = t_r_mm * 1e-3
t_p = t_p_mm * 1e-3
d_cu = d_cu_mm * 1e-3
d_out = d_out_mm * 1e-3
Lw = Lw_mm * 1e-3

Pr_w = mu_w * cp_w / k_w

# Gw through fin model (per wire * number of wires)
A_cu = math.pi * (d_cu**2) / 4.0
P_out = math.pi * d_out

if D_i <= 0 or S <= 0 or k_r <= 0 or k_p <= 0 or A_cu <= 0 or P_out <= 0:
    st.error("–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–µ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã (–ø—Ä–æ–≤–µ—Ä—å—Ç–µ –¥–∏–∞–º–µ—Ç—Ä—ã/–ø–ª–æ—â–∞–¥–∏/—Ç–µ–ø–ª–æ–ø—Ä–æ–≤–æ–¥–Ω–æ—Å—Ç–∏).")
    st.stop()

m_still = math.sqrt(h_still * P_out / (k_cu * A_cu))
Gw_still = n_w * (m_still * k_cu * A_cu * math.tanh(m_still * Lw))

m_draft = math.sqrt(h_draft * P_out / (k_cu * A_cu))
Gw_draft = n_w * (m_draft * k_cu * A_cu * math.tanh(m_draft * Lw))

rows = []
for Q in flows_Lmin:
    Q_m3s = (Q / 1000.0) / 60.0

    # Re = 4 rho Q / (pi mu D)
    Re = 4.0 * rho_w * Q_m3s / (math.pi * mu_w * D_i)

    # Nu: laminar vs turbulent (rough)
    if Re < 2300:
        Nu = 3.66
        regime = "laminar (very rough)"
    elif Re < 4000:
        Nu = 0.023 * (Re**0.8) * (Pr_w**0.4)
        regime = "transition (use with caution)"
    else:
        Nu = 0.023 * (Re**0.8) * (Pr_w**0.4)
        regime = "turbulent"

    h_i = Nu * k_w / D_i

    # R = 1/hi + tr/kr + tp/kp + spread*(tr/kr)
    R = (1.0 / h_i) + (t_r / k_r) + (t_p / k_p) + spread * (t_r / k_r)

    # Conductance to water (W/K)
    K_w = S / R

    # Sensor temperature: Ts = (K_w*Tw + Gw*Tair)/(K_w+Gw)
    Ts_still_K = (K_w * Tw_K + Gw_still * Tair_K) / (K_w + Gw_still)
    Ts_draft_K = (K_w * Tw_K + Gw_draft * Tair_K) / (K_w + Gw_draft)

    Ts_still_C = Ts_still_K - 273.15
    Ts_draft_C = Ts_draft_K - 273.15

    bias_still = Ts_still_K - Tw_K  # Ts - Tw
    bias_draft = Ts_draft_K - Tw_K

    rows.append({
        "–†–∞—Å—Ö–æ–¥, –ª/–º–∏–Ω": Q,
        "Re": Re,
        "–†–µ–∂–∏–º": regime,
        "h·µ¢, –í—Ç/(–º¬≤¬∑–ö)": h_i,
        "R, –º¬≤–ö/–í—Ç": R,
        "K_w, –í—Ç/–ö": K_w,
        "G_w (—Ç–∏—Ö–æ), –í—Ç/–ö": Gw_still,
        "G_w (—Å–∫–≤–æ–∑–Ω—è–∫), –í—Ç/–ö": Gw_draft,
        "T_s (—Ç–∏—Ö–æ), ¬∞C": Ts_still_C,
        "T_s (—Å–∫–≤–æ–∑–Ω—è–∫), ¬∞C": Ts_draft_C,
        "–°–º–µ—â–µ–Ω–∏–µ T_s‚àíT_w (—Ç–∏—Ö–æ), K": bias_still,
        "–°–º–µ—â–µ–Ω–∏–µ T_s‚àíT_w (—Å–∫–≤–æ–∑–Ω—è–∫), K": bias_draft,
        "|—Å–º–µ—â–µ–Ω–∏–µ| (—Ç–∏—Ö–æ), K": abs(bias_still),
        "|—Å–º–µ—â–µ–Ω–∏–µ| (—Å–∫–≤–æ–∑–Ω—è–∫), K": abs(bias_draft),
    })

